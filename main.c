void get_location()
{
	//HAL_UART_Transmit(&huart2,(uint8_t*)tx_data,strlen(tx_data),HAL_MAX_DELAY);
	if(flag == 1)
	{
		MSG_index=0;
		strcpy(tx_data,(char *)(rx_data));
		ptr=strstr(tx_data,"GPRMC");
		if(*ptr == 'G')
		{
			while(1)
			{
				GPS_payload[MSG_index]=*ptr;
				MSG_index++;
				*ptr=*(ptr+MSG_index);
				if(*ptr=='\n')
				{
					GPS_payload[MSG_index]='\0';
					break;
				}
			}
			sscanf(GPS_payload,"GPRMC,%f,A,%f,N,%f,",&time,&latitude,&longitude);
			format_data(time,latitude,longitude);
			HAL_Delay(1);
			flag=0;
		}
	}
}
void format_data(float Time,float lat,float Long)
{
	char data[100];
	hour=(int)time/10000;
	minute=(int)(time-(hour*10000))/100;
	second=(int)(time-((hour*10000)+(minute*100)));
	sprintf(data,"\r\nTime=%d:%d:%d Lat:%f Long=%f",hour+3,minute,second,latitude,longitude);
	HAL_UART_Transmit(&huart2,(uint8_t*)data,strlen(data),HAL_MAX_DELAY);
	HAL_UART_Transmit(&huart2,(uint8_t*)"\r\n\n",3,HAL_MAX_DELAY);
}
